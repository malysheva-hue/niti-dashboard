# глобальный счётчик использования фраз — для диагностики разнообразия
_USAGE = {}


def render_phrase(situation_id: str, **params) -> str:
    """Выбирает случайный вариант фразы и форматирует.

    Перед циклом рендеринга сводки вызвать random.seed(<дата>) для
    идемпотентности в течение дня.

    Пример: render_phrase("order_drop", pct=-42, window=7, start=22, end=13)
    """
    variants = PHRASES.get(situation_id)
    if not variants:
        return f"[phrase missing: {situation_id}]"
    idx = random.randrange(len(variants))
    template = variants[idx]
    _USAGE.setdefault(situation_id, {"calls": 0, "variant_picks": {}})
    _USAGE[situation_id]["calls"] += 1
    _USAGE[situation_id]["variant_picks"].setdefault(idx, 0)
    _USAGE[situation_id]["variant_picks"][idx] += 1
    try:
        return template.format(**params)
    except (KeyError, IndexError) as e:
        return f"[phrase error: {situation_id}: {e}]"


def reset_usage():
    _USAGE.clear()


def get_usage_stats() -> dict:
    """Статистика использования фраз — для проверки разнообразия."""
    return {
        "total_situations_used": len(_USAGE),
        "total_calls": sum(s["calls"] for s in _USAGE.values()),
        "by_situation": {k: v["calls"] for k, v in _USAGE.items()},
        "variants_used": sum(len(s["variant_picks"]) for s in _USAGE.values()),
        "unique_variants_per_situation": {k: len(s["variant_picks"]) for k, v in _USAGE.items() for s in [v]},
    }
